# 算术运算符 + - * /   //（整除）  %(求余数) **（平方）
print("2 + 1 = ", 1 + 1)
print("3 * 3 = ", 3 * 3)
print("11 // 2 = ", 11 // 2)
print("2 ** 2 = ", 2 ** 2)

# 复合运算符
# +=
num = 1
num += 1  # num = num + 1
print("num += 1: ", num)
num -= 1
print("num -= 1: ", num)
num *= 4
print("num *= 4: ", num)
num /= 2
print("num /= 2: ", num)
num = 3
num %= 2
print("num %= 2: ", num)
num **= 2
print("num **= 2:", num)
num = 9
num //= 2
print("num //= 2:", num)

