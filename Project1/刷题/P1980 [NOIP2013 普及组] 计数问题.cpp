#include <iostream>
using namespace std;
typedef long long ll;
int main()
{
	ll n, x, c=0, n1, n2; cin >> n >> x;//c记录出现次数
	for (; n > 0; n--)
	{
		n1 = n2 = n;
		//进入判断, n2用来处理判断
		do
		{
			n2 = n1 % 10;//得到个位
			if (n2 == x)
				c++;
		} while (n1 /= 10);
	}
	cout << c;
	return 0;
}
